# 输入一个四位正整数，求每一位上数字的四次方之和。
# 以输入1234为例，求：1**4+2**4+3**4+4**4的值。
# 输入示例：
# 1234
# 输出示例：
# 354


# 字符串解法
x = input()
a = eval(x[0])
b = eval(x[1])
c = eval(x[2])
d = eval(x[3])
print(a**4+b**4+c**4+d**4)

# 数学
x = eval(input())
a = x // 1000
b = (x // 100) % 10
c = (x // 10) % 10
d = x % 10
print(a**4 + b**4 + c**4 + d**4)